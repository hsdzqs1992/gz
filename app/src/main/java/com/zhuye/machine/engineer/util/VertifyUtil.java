    /**
     * 验证密码
     *
     * @param password
     * @return
     */
    public static boolean isPassword(String password) {

        String num = "^[0-9a-zA-Z]{6,16}$";
        if (TextUtils.isEmpty(password)) {
            return false;
        } else {
            //matches():字符串是否在给定的正则表达式匹配
            return password.matches(num);
        }
    }

